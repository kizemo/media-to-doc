"""Stage 9 — ``render``:拼装最终讲义 markdown + HTML。

输入:
- ``work/chapters/chapters.json``:章节元数据(title / summary / key_points / image_refs)
- ``<drafts_dir>/chapter_NN.md``:每章节草稿 markdown(由 :mod:`draft` 产出,
  ``[[GEN: ...]]`` 已被 :mod:`imagegen` 替换为 ``![[gen_<uuid>.png]]``)
- ``<drafts_dir>/images/gen_<uuid>.png``:AI 配图(可能缺失)

输出:
- ``<output_dir>/<stem>.md``:拼装后的完整讲义 markdown(含 TOC)
- ``<output_dir>/<stem>.html``:渲染后的 HTML(jinja2 模板,TOC + 内嵌 CSS)

布局约定::

    <output_dir>/
    ├── <stem>.md
    ├── <stem>.html
    └── <stem>/
        ├── chapter_NN.md        (可选保留,作为参考)
        └── images/
            └── gen_<uuid>.png

行为:
1. 从 ``chapters.json`` 派生 ``video / stem``
2. 每章节:读 ``chapter_NN.md`` → 重写图像引用为标准 markdown 语法
   ``![[gen_xxx.png]]`` → ``![Image](<stem>/images/gen_xxx.png)``
3. 拼装 markdown:TOC + 各章节块(含标题 / 摘要 / 要点 / 关键帧链接 / 章节正文)
4. 用 markdown 库 + jinja2 模板生成 HTML(TOC 锚点、内嵌 CSS)
5. 所有产物路径相对 ``<stem>.md / .html`` 自身,不依赖绝对路径

依赖:
- :mod:`markdown`(md → HTML,PIP 运行时)
- jinja2 模板(字符串模板,无外部文件)
- :mod:`chapters` 的 :class:`ChaptersReport`(复用)

参考:TDD §5 数据流第 9 步 + PROJECT_DESCRIPTION §3.2 render 行 +
     CLAUDE.md §7 图片相对路径红线。
"""

