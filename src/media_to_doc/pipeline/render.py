def _assemble_markdown(
  report: ChaptersReport,
  body_lookup: dict[int, str],
) -> str:
  """жӢјиЈ…е®Ңж•ҙ markdown:TOC + иҜҫзЁӢж Үйўҳ + жҜҸз« иҠӮеқ—гҖӮ"""
  parts: list[str] = []
  parts.append(f"# {report.video or 'иҜҫзЁӢи®Ід№ү'}")
  parts.append("")
  parts.append("> жң¬и®Ід№үз”ұ media-to-doc жөҒж°ҙзәҝиҮӘеҠЁз”ҹжҲҗгҖӮ")
  parts.append("")
  parts.append(_build_toc(report.chapters))
  parts.append("---")
  parts.append("")

  for ch in report.chapters:
    parts.append(f"## {ch.title}")
    parts.append("")
    if ch.summary:
      parts.append(f"> **ж‘ҳиҰҒ**:{ch.summary}")
      parts.append("")
    if ch.key_points:
      parts.append("**е…ій”®иҰҒзӮ№**:")
      parts.append("")
      for p in ch.key_points:
        parts.append(f"- {p}")
      parts.append("")
    if ch.image_refs:
      parts.append("**еј•з”Ёе…ій”®её§**:")
      parts.append("")
      for ts in ch.image_refs:
        parts.append(f"- {ts:.2f}s вҶ’ `frame_{int(ts * 1000):09d}.jpg`")
      parts.append("")
    body = body_lookup.get(ch.idx, "")
    if body:
      body = _strip_timestamp_lines(body).strip()
      parts.append(body)
      parts.append("")
    parts.append("---")
    parts.append("")
  return "\n".join(parts).rstrip() + "\n"


