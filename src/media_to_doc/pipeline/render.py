def _read_chapter_body(drafts_dir: Path, idx: int) -> str:
  """иҜ» ``chapter_NN.md`` зҡ„жӯЈж–Ү(H1 ж ҮйўҳиЎҢеҸҠе…¶д»ҘдёӢзҡ„жүҖжңүеҶ…е®№,еүҘзҰ»ж‘ҳиҰҒ/иҰҒзӮ№еқ—)гҖӮ"""
  md_path = drafts_dir / f"chapter_{idx:02d}.md"
  if not md_path.exists():
    return ""
  text = md_path.read_text(encoding="utf-8")
  # жүҫеҲ° ``\n---\n`` д№ӢеҗҺзҡ„жӯЈж–Ү(pipeline иҮӘе·ұзҡ„ ``_render_draft_markdown`` жЁЎејҸ)
  parts = text.split("\n---\n", 1)
  return parts[1].lstrip("\n").rstrip() if len(parts) == 2 else text.rstrip()


def _rewrite_image_refs(body: str, image_prefix: str, drafts_dir: Path) -> tuple[str, int, int]:
  """жҠҠ ``![[gen_xxx.png]]`` ж”№еҶҷдёә ``![Image](<image_prefix>/gen_xxx.png)``гҖӮ

  - зјәеӨұзҡ„еӣҫеғҸ(дёҚеңЁ ``drafts_dir/images/``)вҶ’ ж”№еҶҷдёәиӯҰе‘Ҡж–Үеӯ—,дёҚиҫ“еҮә broken link
  - иҝ”еӣһ (rewritten, resolved_count, missing_count)
  """
  resolved = 0
  missing = 0

  def _repl(m: re.Match[str]) -> str:
    nonlocal resolved, missing
    name = m.group(1).strip()
    img_path = drafts_dir / "images" / name
    if img_path.exists():
      resolved += 1
      return f"![Image]({image_prefix}/{name})"
    missing += 1
    return f"_вҡ пёҸ й…ҚеӣҫзјәеӨұ:{name}_"

  out = _WIKILINK_IMG_RE.sub(_repl, body)
  return out, resolved, missing


def _strip_timestamp_lines(body: str) -> str:
  """еҺ»жҺү ``[start-ends] text`` еҪўејҸзҡ„ж—¶й—ҙжҲіеҚ дҪҚ(еҰӮжһңжҳҜйҖҗеӯ—зЁҝеј•з”Ёж®Ӣз•ҷ)гҖӮ"""
  return _TIMESTAMP_LINE_RE.sub("", body)


