def load_chapters_report(path: Path) -> ChaptersReport:
  """д»Һ ``chapters.json`` еҸҚеәҸеҲ—еҢ–(дёҺ ``draft.py`` зӯүд»·зҡ„ helper)гҖӮ"""
  data = json.loads(path.read_text(encoding="utf-8"))
  chapters: list[Chapter] = []
  for raw in data.get("chapters", []):
    payload = {k: v for k, v in raw.items() if k != "duration_seconds"}
    chapters.append(Chapter(**payload))
  return ChaptersReport(
    video=data.get("video", ""),
    provider=data.get("provider", ""),
    model=data.get("model", ""),
    chapters=chapters,
  )


def render_outputs(
  work: Path,
  config: WorkflowConfig | None = None,
  *,
  chapters_dir: Path | None = None,
  drafts_dir: Path | None = None,
  output_dir: Path | None = None,
  output_stem: str | None = None,
  final_dir: Path | None = None,
  write_html: bool = True,
) -> RenderOutputs:
  """Stage 9:жӢјиЈ…жңҖз»Ҳи®Ід№ү markdown (+ еҸҜйҖү HTML)гҖӮ

  W12-D ж–°и§„:жңҖз»Ҳдә§зү©еҶҷеҲ° ``final_dir``(й»ҳи®Ө ``<work>.parent / "output_final"``),
  дёҺдёӯй—ҙдә§зү© ``<work>`` еҲҶзҰ»гҖӮ

  еёғеұҖзәҰе®ҡ(иҮӘеҢ…еҗ«,еҸҜж•ҙзӣҳеҲҶеҸ‘):

  ::

      <final_dir>/
      в”ңв”Җв”Җ <stem>.md           вҶҗ жӢјиЈ…и®Ід№ү markdown
      в”ңв”Җв”Җ <stem>.html         вҶҗ жёІжҹ“ HTML
      в””в”Җв”Җ <stem>/
          в””в”Җв”Җ images/         вҶҗ д»Һ drafts_dir/images/ еӨҚеҲ¶иҝҮжқҘ

  жңҖз»Ҳдә§зү©з”Ёзңҹи§Ҷйў‘еҗҚ(``chapters.json video``)е‘ҪеҗҚ,еӣҫеғҸеј•з”ЁдҝқжҢҒ
  ``<stem>/images/gen_xxx.png`` зӣёеҜ№и·Ҝеҫ„дёҚеҸҳгҖӮ

  Parameters
  ----------
  work : Path
    work ж №зӣ®еҪ•(дёӯй—ҙдә§зү©ж №)
  config : WorkflowConfig | None
    й…ҚзҪ®(йў„з•ҷ)
  chapters_dir : Path | None
    ``chapters.json`` жүҖеңЁзӣ®еҪ•(й»ҳи®Ө ``<work>/chapters``)
  drafts_dir : Path | None
    ``chapter_NN.md`` жүҖеңЁзӣ®еҪ•(й»ҳи®Ө ``<chapters_dir>/raw/<video_stem>``)
  output_dir : Path | None
    **дҝқз•ҷеҸӮж•°**вҖ”вҖ”е…је®№жҖ§ fallbackгҖӮдјҳе…Ҳзә§дҪҺдәҺ ``final_dir``гҖӮ
  output_stem : str | None
    иҫ“еҮәж–Үд»¶еҗҚ(й»ҳи®Ө = ``chapters.json video`` еӯ—ж®ө,W12-D иө·еҚізңҹи§Ҷйў‘еҗҚ)
  final_dir : Path | None
    W12-D ж–°еўһ:жңҖз»Ҳдә§зү©зӣ®еҪ•(й»ҳи®Ө ``<work>.parent / "output_final"``)гҖӮ
    ``<final_dir>/<output_stem>.md`` + ``.html`` + ``<final_dir>/<output_stem>/images/`` еҶҷеҲ°жӯӨеӨ„гҖӮ
  write_html : bool
    жҳҜеҗҰеҗҢж—¶еҶҷ HTML(й»ҳи®Ө True)

  Returns
  -------
  RenderOutputs
    еҗ« md_path / html_path / жёІжҹ“з»ҹи®Ў
  """
  _ = config
  c_dir = chapters_dir or (work / "chapters")
  chapters_json = c_dir / "chapters.json"
  if not chapters_json.exists():
    raise FileNotFoundError(
      f"жүҫдёҚеҲ° {chapters_json};иҜ·е…Ҳи·‘ chapters stage"
    )
  report = load_chapters_report(chapters_json)
  if not report.chapters:
    raise ValueError("chapters.json з« иҠӮеҲ—иЎЁдёәз©ә,ж— жі•жёІжҹ“")

  if drafts_dir is None:
    drafts_dir = c_dir / "raw" / (report.video or "output")
  if not drafts_dir.exists():
    raise FileNotFoundError(
      f"жүҫдёҚеҲ° drafts_dir {drafts_dir};иҜ·е…Ҳи·‘ draft stage"
    )

  # W12-D:output_stem дјҳе…Ҳз”Ё report.video(зңҹи§Ҷйў‘еҗҚ)
  if output_stem is None:
    output_stem = report.video or "output"

  # W12-D:жңҖз»Ҳдә§зү©зӣ®еҪ•(й»ҳи®Ө <work>.parent / "output_final")
  if final_dir is None:
    final_dir = work.parent / "output_final"
  final_dir.mkdir(parents=True, exist_ok=True)

  # е…је®№ж—§и°ғз”Ё:иӢҘ output_dir жҳҫејҸдј дәҶ,дјҳе…Ҳз”Ё(еҗ‘еҗҺе…је®№)
  target_dir = output_dir or final_dir

  # W12-D:жҠҠ drafts_dir/images/ еӨҚеҲ¶еҲ° final_dir/<stem>/images/(иҮӘеҢ…еҗ«)
  # е…Ҳе»әеӯҗзӣ®еҪ•,еҶҚеӨҚеҲ¶(йҒҝе…ҚеҗҺйқў image еј•з”Ё broken)
  final_subdir = target_dir / output_stem
  final_images = final_subdir / "images"
  final_images.parent.mkdir(parents=True, exist_ok=True)
  drafts_images = drafts_dir / "images"
  if drafts_images.exists():
    import shutil

    if final_images.exists():
      shutil.rmtree(final_images)
    shutil.copytree(drafts_images, final_images)

  # и®Ўз®—еӣҫеғҸзӣёеҜ№и·Ҝеҫ„еүҚзјҖ(д»Һ ``<target_dir>/<stem>.md`` еҲ° ``<stem>/images/``):
  # ``image_prefix`` = "<stem>/images"(дёҺ W3 ж—ўжңүеёғеұҖдёҖиҮҙ)
  image_prefix = f"{output_stem}/images"

  # иҜ»еҸ–жҜҸз« иҠӮжӯЈж–Ү + йҮҚеҶҷеӣҫеғҸеј•з”Ё
  body_lookup: dict[int, str] = {}
  resolved_total = 0
  missing_total = 0
  for ch in report.chapters:
    raw_body = _read_chapter_body(drafts_dir, ch.idx)
    if not raw_body:
      continue
    rewritten, resolved, missing = _rewrite_image_refs(
      raw_body, image_prefix, drafts_dir
    )
    body_lookup[ch.idx] = rewritten
    resolved_total += resolved
    missing_total += missing

  # жӢјиЈ… markdown
  md_text = _assemble_markdown(report, body_lookup)
  md_path = target_dir / f"{output_stem}.md"
  md_path.write_text(md_text, encoding="utf-8")

  html_path: Path | None = None
  if write_html:
    body_html = _render_markdown_to_html(md_text)
    full_html = _wrap_html_body(body_html, report.video or output_stem)
    html_path = target_dir / f"{output_stem}.html"
    html_path.write_text(full_html, encoding="utf-8")

  return RenderOutputs(
    video=report.video,
    course_title=output_stem,
    md_path=md_path,
    html_path=html_path,
    chapters_rendered=len(body_lookup),
    image_refs_resolved=resolved_total,
    image_refs_missing=missing_total,
  )


def render_html(md_path: Path, *, html_path: Path | None = None) -> Path:
  """зӢ¬з«ӢеҮҪж•°:д»ҺзҺ°жңү ``.md`` жёІжҹ“ ``.html``(дҫҝдәҺ ``read_lecture`` MCP е·Ҙе…·и°ғз”Ё)гҖӮ

  Parameters
  ----------
  md_path : Path
    е·ІеӯҳеңЁзҡ„ ``<stem>.md`` и·Ҝеҫ„
  html_path : Path | None
    иҫ“еҮәи·Ҝеҫ„(й»ҳи®Ө ``<md_path>.stem``.html)

  Returns
  -------
  Path
    е®һйҷ…еҶҷе…Ҙзҡ„ html и·Ҝеҫ„
  """
  if not md_path.exists():
    raise FileNotFoundError(f"жүҫдёҚеҲ° {md_path}")
  md_text = md_path.read_text(encoding="utf-8")
  body_html = _render_markdown_to_html(md_text)
  title = md_path.stem
  full_html = _wrap_html_body(body_html, title)
  target = html_path or md_path.with_suffix(".html")
  target.write_text(full_html, encoding="utf-8")
  return target


