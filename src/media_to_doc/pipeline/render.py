@dataclass
class RenderOutputs:
  """整体渲染结果。"""

  video: str = ""
  course_title: str = ""
  md_path: Path | None = None
  html_path: Path | None = None
  chapters_rendered: int = 0
  image_refs_resolved: int = 0
  image_refs_missing: int = 0

  def to_dict(self) -> dict[str, object]:
    return {
      "video": self.video,
      "course_title": self.course_title,
      "md_path": str(self.md_path) if self.md_path else "",
      "html_path": str(self.html_path) if self.html_path else "",
      "chapters_rendered": self.chapters_rendered,
      "image_refs_resolved": self.image_refs_resolved,
      "image_refs_missing": self.image_refs_missing,
    }


# ─────────────────────────────────────────────────────────────
# 章节正文读取与图像引用重写
# ─────────────────────────────────────────────────────────────


def _read_chapter_body(drafts_dir: Path, idx: int) -> str:
  """读 ``chapter_NN.md`` 的正文(H1 标题行及其以下的所有内容,剥离摘要/要点块)。"""
  md_path = drafts_dir / f"chapter_{idx:02d}.md"
  if not md_path.exists():
    return ""
  text = md_path.read_text(encoding="utf-8")
  # 找到 ``\n---\n`` 之后的正文(pipeline 自己的 ``_render_draft_markdown`` 模式)
  parts = text.split("\n---\n", 1)
  return parts[1].lstrip("\n").rstrip() if len(parts) == 2 else text.rstrip()


def _rewrite_image_refs(body: str, image_prefix: str, drafts_dir: Path) -> tuple[str, int, int]:
  """把 ``![[gen_xxx.png]]`` 改写为 ``![Image](<image_prefix>/gen_xxx.png)``。

  - 缺失的图像(不在 ``drafts_dir/images/``)→ 改写为警告文字,不输出 broken link
  - 返回 (rewritten, resolved_count, missing_count)
  """
  resolved = 0
  missing = 0

  def _repl(m: re.Match[str]) -> str:
    nonlocal resolved, missing
    name = m.group(1).strip()
    img_path = drafts_dir / "images" / name
    if img_path.exists():
      resolved += 1
      return f"![Image]({image_prefix}/{name})"
    missing += 1
    return f"_⚠️ 配图缺失:{name}_"

  out = _WIKILINK_IMG_RE.sub(_repl, body)
  return out, resolved, missing


def _strip_timestamp_lines(body: str) -> str:
  """去掉 ``[start-ends] text`` 形式的时间戳占位(如果是逐字稿引用残留)。"""
  return _TIMESTAMP_LINE_RE.sub("", body)


# ─────────────────────────────────────────────────────────────
# markdown / HTML 渲染
# ─────────────────────────────────────────────────────────────


def _build_toc(chapters: list[Chapter]) -> str:
  """生成 ``\n- [title](#anchor)\n`` 形式的 TOC markdown。"""
  lines: list[str] = []
  lines.append("## 目录")
  lines.append("")
  for ch in chapters:
    # markdown 库的 toc 扩展会自动从 H1/H2 派生 anchor
    # 我们这里手工做 link,文本对应 ``## {title}``
    slug = _slugify(ch.title)
    lines.append(f"- [{ch.title}](#{slug})")
  lines.append("")
  return "\n".join(lines)


_SLUG_RE = re.compile(r"[^a-zA-Z0-9\u4e00-\u9fff\-_]+")


def _slugify(text: str) -> str:
  """生成 markdown 锚点兼容的 slug:小写 + 非字面字符替 ``-``。"""
  s = text.strip().lower()
  s = _SLUG_RE.sub("-", s)
  s = s.strip("-")
  return s or "section"


def _render_markdown_to_html(md_text: str) -> str:
  """md → html(启用 toc 扩展 + fenced_code + tables)。"""
  md = md_lib.Markdown(
    extensions=[
      "fenced_code",
      "tables",
      TocExtension(toc_depth="2-3"),
    ],
    output_format="html",
  )
  return md.convert(md_text)


_FIRST_H1_RE = re.compile(r"<h1[^>]*>(.*?)</h1>", re.DOTALL | re.IGNORECASE)
_TAG_STRIP_RE = re.compile(r"<[^>]+>")


def _extract_first_h1_text(body_html: str) -> str:
  """从渲染后的 body_html 提取首个 ``<h1>`` 标签的纯文本(去除内嵌标签)。

  用纯 stdlib regex,避免给 render stage 增加 BeautifulSoup 依赖(那是
  longdoc extras)。

  用于 v1.2.0+ 解决 ``<title>`` 与首个 H1 不一致的 verify warning:
  当 chapters.video 不可用 / 退化 "output" 时,fallback 到首个 H1 真实文本。
  """
  m = _FIRST_H1_RE.search(body_html)
  if not m:
    return ""
  text = _TAG_STRIP_RE.sub("", m.group(1))
  return text.strip()


def _resolve_title(
  report_video: str,
  body_html: str,
  fallback_stem: str,
) -> str:
  """resolve HTML ``<title>`` 的最终值。

  优先级(W12-F):
  1. ``report_video``(真视频名,chapters.video)— 优先,避免依赖后续 markdown
  2. body_html 首个 H1 文本(`_extract_first_h1_text`)— fallback,确保
     ``<title>`` 与首个 H1 一致(消除 verify warning)
  3. ``fallback_stem``(通常 = output_stem)— 兜底

  Returns
  -------
  str
    非空 title(至少 = fallback_stem)
  """
  v = (report_video or "").strip()
  if v:
    return v
  h1 = _extract_first_h1_text(body_html)
  if h1:
    return h1
  return (fallback_stem or "").strip() or "output"


def _wrap_html_body(body_html: str, title: str) -> str:
  """用 jinja2 模板把 body 包成完整 HTML。"""
  import datetime as _dt

  return _HTML_TEMPLATE.render(
    title=title,
    body=body_html,
    generated_at=_dt.datetime.now().strftime("%Y-%m-%d %H:%M"),
  )


# ─────────────────────────────────────────────────────────────
# Markdown 拼装
# ─────────────────────────────────────────────────────────────


def _assemble_markdown(
  report: ChaptersReport,
  body_lookup: dict[int, str],
) -> str:
  """拼装完整 markdown:TOC + 课程标题 + 每章节块。"""
  parts: list[str] = []
  parts.append(f"# {report.video or '课程讲义'}")
  parts.append("")
  parts.append("> 本讲义由 media-to-doc 流水线自动生成。")
  parts.append("")
  parts.append(_build_toc(report.chapters))
  parts.append("---")
  parts.append("")

  for ch in report.chapters:
    parts.append(f"## {ch.title}")
    parts.append("")
    if ch.summary:
      parts.append(f"> **摘要**:{ch.summary}")
      parts.append("")
    if ch.key_points:
      parts.append("**关键要点**:")
      parts.append("")
      for p in ch.key_points:
        parts.append(f"- {p}")
      parts.append("")
    if ch.image_refs:
      parts.append("**引用关键帧**:")
      parts.append("")
      for ts in ch.image_refs:
        parts.append(f"- {ts:.2f}s → `frame_{int(ts * 1000):09d}.jpg`")
      parts.append("")
    body = body_lookup.get(ch.idx, "")
    if body:
      body = _strip_timestamp_lines(body).strip()
      parts.append(body)
      parts.append("")
    parts.append("---")
    parts.append("")
  return "\n".join(parts).rstrip() + "\n"


# ─────────────────────────────────────────────────────────────
# 公开 API
# ─────────────────────────────────────────────────────────────


def load_chapters_report(path: Path) -> ChaptersReport:
  """从 ``chapters.json`` 反序列化(与 ``draft.py`` 等价的 helper)。"""
  data = json.loads(path.read_text(encoding="utf-8"))
  chapters: list[Chapter] = []
  for raw in data.get("chapters", []):
    payload = {k: v for k, v in raw.items() if k != "duration_seconds"}
    chapters.append(Chapter(**payload))
  return ChaptersReport(
    video=data.get("video", ""),
    provider=data.get("provider", ""),
    model=data.get("model", ""),
    chapters=chapters,
  )


def render_outputs(
  work: Path,
  config: WorkflowConfig | None = None,
  *,
  chapters_dir: Path | None = None,
  drafts_dir: Path | None = None,
  output_dir: Path | None = None,
  output_stem: str | None = None,
  final_dir: Path | None = None,
  write_html: bool = True,
) -> RenderOutputs:
  """Stage 9:拼装最终讲义 markdown (+ 可选 HTML)。

  W12-D 新规:最终产物写到 ``final_dir``(默认 ``<work>.parent / "output_final"``),
  与中间产物 ``<work>`` 分离。

  布局约定(自包含,可整盘分发):

  ::

      <final_dir>/
      ├── <stem>.md           ← 拼装讲义 markdown
      ├── <stem>.html         ← 渲染 HTML
      └── <stem>/
          └── images/         ← 从 drafts_dir/images/ 复制过来

  最终产物用真视频名(``chapters.json video``)命名,图像引用保持
  ``<stem>/images/gen_xxx.png`` 相对路径不变。

  Parameters
  ----------
  work : Path
    work 根目录(中间产物根)
  config : WorkflowConfig | None
    配置(预留)
  chapters_dir : Path | None
    ``chapters.json`` 所在目录(默认 ``<work>/chapters``)
  drafts_dir : Path | None
    ``chapter_NN.md`` 所在目录(默认 ``<chapters_dir>/raw/<video_stem>``)
  output_dir : Path | None
    **保留参数**——兼容性 fallback。优先级低于 ``final_dir``。
  output_stem : str | None
    输出文件名(默认 = ``chapters.json video`` 字段,W12-D 起即真视频名)
  final_dir : Path | None
    W12-D 新增:最终产物目录(默认 ``<work>.parent / "output_final"``)。
    ``<final_dir>/<output_stem>.md`` + ``.html`` + ``<final_dir>/<output_stem>/images/`` 写到此处。
  write_html : bool
    是否同时写 HTML(默认 True)

  Returns
  -------
  RenderOutputs
    含 md_path / html_path / 渲染统计
  """
  _ = config
  c_dir = chapters_dir or (work / "chapters")
  chapters_json = c_dir / "chapters.json"
  if not chapters_json.exists():
    raise FileNotFoundError(
      f"找不到 {chapters_json};请先跑 chapters stage"
    )
  report = load_chapters_report(chapters_json)
  if not report.chapters:
    raise ValueError("chapters.json 章节列表为空,无法渲染")

  if drafts_dir is None:
    drafts_dir = c_dir / "raw" / (report.video or "output")
  if not drafts_dir.exists():
    raise FileNotFoundError(
      f"找不到 drafts_dir {drafts_dir};请先跑 draft stage"
    )

  # W12-D:output_stem 优先用 report.video(真视频名)
  if output_stem is None:
    output_stem = report.video or "output"

  # W12-D:最终产物目录(默认 <work>.parent / "output_final")
  if final_dir is None:
    final_dir = work.parent / "output_final"
  final_dir.mkdir(parents=True, exist_ok=True)

  # 兼容旧调用:若 output_dir 显式传了,优先用(向后兼容)
  target_dir = output_dir or final_dir

  # W12-D:把 drafts_dir/images/ 复制到 final_dir/<stem>/images/(自包含)
  # 先建子目录,再复制(避免后面 image 引用 broken)
  final_subdir = target_dir / output_stem
  final_images = final_subdir / "images"
  final_images.parent.mkdir(parents=True, exist_ok=True)
  drafts_images = drafts_dir / "images"
  if drafts_images.exists():
    import shutil

    if final_images.exists():
      shutil.rmtree(final_images)
    shutil.copytree(drafts_images, final_images)

  # 计算图像相对路径前缀(从 ``<target_dir>/<stem>.md`` 到 ``<stem>/images/``):
  # ``image_prefix`` = "<stem>/images"(与 W3 既有布局一致)
  image_prefix = f"{output_stem}/images"

  # 读取每章节正文 + 重写图像引用
  body_lookup: dict[int, str] = {}
  resolved_total = 0
  missing_total = 0
  for ch in report.chapters:
    raw_body = _read_chapter_body(drafts_dir, ch.idx)
    if not raw_body:
      continue
    rewritten, resolved, missing = _rewrite_image_refs(
      raw_body, image_prefix, drafts_dir
    )
    body_lookup[ch.idx] = rewritten
    resolved_total += resolved
    missing_total += missing

  # 拼装 markdown
  md_text = _assemble_markdown(report, body_lookup)
  md_path = target_dir / f"{output_stem}.md"
  md_path.write_text(md_text, encoding="utf-8")

  html_path: Path | None = None
  if write_html:
    body_html = _render_markdown_to_html(md_text)
    full_html = _wrap_html_body(
      body_html,
      _resolve_title(report.video, body_html, output_stem),
    )
    html_path = target_dir / f"{output_stem}.html"
    html_path.write_text(full_html, encoding="utf-8")

  return RenderOutputs(
    video=report.video,
    course_title=output_stem,
    md_path=md_path,
    html_path=html_path,
    chapters_rendered=len(body_lookup),
    image_refs_resolved=resolved_total,
    image_refs_missing=missing_total,
  )


def render_html(md_path: Path, *, html_path: Path | None = None) -> Path:
  """独立函数:从现有 ``.md`` 渲染 ``.html``(便于 ``read_lecture`` MCP 工具调用)。

  Parameters
  ----------
  md_path : Path
    已存在的 ``<stem>.md`` 路径
  html_path : Path | None
    输出路径(默认 ``<md_path>.stem``.html)

  Returns
  -------
  Path
    实际写入的 html 路径
  """
  if not md_path.exists():
    raise FileNotFoundError(f"找不到 {md_path}")
  md_text = md_path.read_text(encoding="utf-8")
  body_html = _render_markdown_to_html(md_text)
  title = _resolve_title("", body_html, md_path.stem)
  full_html = _wrap_html_body(body_html, title)
  target = html_path or md_path.with_suffix(".html")
  target.write_text(full_html, encoding="utf-8")
  return target


