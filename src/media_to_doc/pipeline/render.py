# ``![[gen_<uuid>.png]]`` 图像 wiki-link → 标准 markdown 图片
_WIKILINK_IMG_RE = re.compile(r"!\[\[([^\]]+\.png)\]\]")

# ``[start-ends] text`` 时间戳占位(章节 md 末尾可能有)
# 允许行首空白以兼容 ``   [0.50s - 1.20s] text``
_TIMESTAMP_LINE_RE = re.compile(r"^\s*\[[^\]]*\d[^\]]*\].*$", re.MULTILINE)

