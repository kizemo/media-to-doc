def _build_toc(chapters: list[Chapter]) -> str:
  """з”ҹжҲҗ ``\n- [title](#anchor)\n`` еҪўејҸзҡ„ TOC markdownгҖӮ"""
  lines: list[str] = []
  lines.append("## зӣ®еҪ•")
  lines.append("")
  for ch in chapters:
    # markdown еә“зҡ„ toc жү©еұ•дјҡиҮӘеҠЁд»Һ H1/H2 жҙҫз”ҹ anchor
    # жҲ‘д»¬иҝҷйҮҢжүӢе·ҘеҒҡ link,ж–Үжң¬еҜ№еә” ``## {title}``
    slug = _slugify(ch.title)
    lines.append(f"- [{ch.title}](#{slug})")
  lines.append("")
  return "\n".join(lines)


_SLUG_RE = re.compile(r"[^a-zA-Z0-9\u4e00-\u9fff\-_]+")


def _slugify(text: str) -> str:
  """з”ҹжҲҗ markdown й”ҡзӮ№е…је®№зҡ„ slug:е°ҸеҶҷ + йқһеӯ—йқўеӯ—з¬Ұжӣҝ ``-``гҖӮ"""
  s = text.strip().lower()
  s = _SLUG_RE.sub("-", s)
  s = s.strip("-")
  return s or "section"


def _render_markdown_to_html(md_text: str) -> str:
  """md вҶ’ html(еҗҜз”Ё toc жү©еұ• + fenced_code + tables)гҖӮ"""
  md = md_lib.Markdown(
    extensions=[
      "fenced_code",
      "tables",
      TocExtension(toc_depth="2-3"),
    ],
    output_format="html",
  )
  return md.convert(md_text)


def _wrap_html_body(body_html: str, title: str) -> str:
  """з”Ё jinja2 жЁЎжқҝжҠҠ body еҢ…жҲҗе®Ңж•ҙ HTMLгҖӮ"""
  import datetime as _dt

  return _HTML_TEMPLATE.render(
    title=title,
    body=body_html,
    generated_at=_dt.datetime.now().strftime("%Y-%m-%d %H:%M"),
  )


